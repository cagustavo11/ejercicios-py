"""
21. Muestra los números múltiplos de 5 de 0 a 100 utilizando un bucle for. Muestra los números múltiplos de 5 de 0 a 100 utilizando un bucle while.
"""
print(f'---------------FOR---------------')
for i in range(0, 100+5, 5):
    print(i)


i = 0
print(f'---------------WHILE---------------')
while i < 105:
    print(i)

    i += 5
